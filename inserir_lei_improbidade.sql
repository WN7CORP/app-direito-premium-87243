§ 8º A pessoa jurídica prejudicada poderá ajuizar ação para a aplicação das sanções previstas nesta Lei.

§ 9º Nas ações de que trata esta Lei, será permitido ao magistrado responsável pela ação principal, de ofício ou mediante requerimento das partes, estabelecer regime de publicidade especial para a tramitação do feito, mediante restrição de acesso aos autos, a fim de assegurar o sigilo necessário à elucidação do fato ou exigido pelo interesse da sociedade ou do Estado.

