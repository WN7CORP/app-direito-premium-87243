§ 13. Observado o devido processo legal, é facultado aos órgãos e entidades públicas lesadas pelo ato de improbidade administrativa, quando à ação principal não tiver sido atribuído o rito previsto na Lei nº 7.347, de 24 de julho de 1985, a execução provisória, desde o oferecimento da petição inicial, das medidas de:

I - afastamento do agente público do exercício do cargo, do emprego ou da função, sem prejuízo da remuneração, quando a medida se mostrar necessária à instrução processual ou para evitar a reiteração de atos de improbidade;

II - indisponibilidade dos bens do demandado, nos termos do § 1º do art. 7º desta Lei.

